package Day01;
 
public class 연산자문제 {
 
    public static void main(String[] args) {
        /* num1 과 num2  연산자 1개 를 출력
         * num1 = 1, num2 = 2, 연산자 + 
         *  1 + 2
         * 클럽에 저장
         * */
        int num1 = 5;
        int num2 = 3;
        char ch = '*';
        
        System.out.println(num1 + " " + ch +" "+ num2);
        
    }
 
}
